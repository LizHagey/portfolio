    <h3>Window Graphics</h3><br>
    <h3>Before</h3>
    <p>Customer wanted our sign company to redo his window lettering because his windows face 
        the afternoon sun and the vinyl had become burnt and discolored (the Vegas sun is brutal).</p>
    <img class="windowGraphics" src="/media/Before.jpg" alt="Previous window lettering">
    <br><br>
    <h3>The Design</h3>
    <p>Because his windows face west the customer had taken great measures to block the sun from 
        making his waiting room too warm.  I went out on a limb and instead of simply redoing the 
        window lettering I designed window perf on all of the windows.  This would help block the 
        sun from coming into the waiting area, make the message more clear, and make his business 
        standout among the other businesses in the center.</p>
    <img class="windowGraphics" src="/media/chiroDesign.jpg" alt="New design for window">
    <br><br>
    <h3>Final Result</h3>
    <p>The customer agreed when he saw the layouts I had designed that the window perf was a better 
        option and although it would cost him extra money he saw the value in doing
        it.  We closed a deal for the window perf and had a beautiful finished product
        that stands out, potentially leading to new customers.</p>
    <img class="windowGraphics" src="/media/chiroWindow.jpg" alt="Graphics applied to window">
    <br><br><br>
    
    
    <h3>Banner designs</h3>
    <p>Customer was unhappy with a banner he ordered through a different company and wanted to see what we 
        could do.  Customer was very nice, but very particular when it came to this banner.</p>
    <p>After emailing a couple of layouts, I went to his office with my laptop.  I hooked it up to his 
        large screen and adjusted the banner in his office to his liking.  I had put the layout on the 
        building so that he could see what the final product would look like when installed and up close.  
        I spent more time than normal on the design but we wanted to make him happy.</p>
    <p>The result was a banner that can be seen and read from the nearby freeway and a 
    happy customer.</p>
