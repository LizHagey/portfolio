    <h3>Window Graphics</h3>
    <h3>Before</h3>
    <p>Customer wanted our sign company to redo his window lettering because his windows face 
        the afternoon sun and the vinyl had become burnt and discolored (the Vegas sun is brutal).</p>
    <img id="windowGraphics" src="/portfolio/media/Before.jpg" alt="Previous window lettering">
    <h3>The Design</h3>
    <p>Because his windows face west the customer had taken great measures to block the sun from 
        making his waiting room too warm.  I went out on a limb and instead of simply redoing the 
        window lettering I designed window perf on all of the windows.  This would help block the 
        sun from coming into the waiting area, make the message more clear, and make his business 
        standout among the other businesses in the center.</p>
    <img id="windowGraphics" src="/portfolio/media/chiroDesign.jpg" alt="New design for window">
    <h3>Final Result</h3>
    <p>The customer agreed when he saw the layouts I had designed that the window perf was a better 
        option and although it would cost him extra money he saw the value in doing
        it.  We closed a deal for the window perf and had a beautiful finished product
        that stands out, potentially leading to new customers.</p>
    <img id="windowGraphics" src="/portfolio/media/chiroWindow.jpg" alt="Graphics applied to window">
    
